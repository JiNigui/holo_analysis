// 完整的Marching Cubes算法实现
function generateMarchingCubesMesh(voxelArray, dimensions, isoLevel = 0.5) {
  const [width, height, depth] = dimensions
  const vertices = []
  const indices = []
  const vertexMap = new Map()
  
  console.log(`Worker: 开始Marching Cubes计算: ${width}x${height}x${depth}`)
  
  const totalCubes = (width - 1) * (height - 1) * (depth - 1)
  let processedCubes = 0
  let lastProgress = 0
  
  // 发送初始进度
  self.postMessage({
    type: 'progress',
    progress: 0
  })
  
  console.log(`Worker: 开始遍历体素单元，总计${totalCubes}个立方体`)
  
  // 遍历所有体素单元
  for (let z = 0; z < depth - 1; z++) {
    for (let y = 0; y < height - 1; y++) {
      for (let x = 0; x < width - 1; x++) {
        
        // 调试：每10000个立方体输出一次进度
        if (processedCubes % 10000 === 0) {
          console.log(`Worker: 处理到第${processedCubes}个立方体 (${(processedCubes/totalCubes*100).toFixed(2)}%)`)
        }
        // 获取当前立方体8个顶点的值
        const cubeValues = getCubeValues(voxelArray, x, y, z, width, height)
        
        // 计算立方体配置索引
        const cubeIndex = getCubeIndex(cubeValues, isoLevel)
        
        // 如果立方体与等值面相交
        if (cubeIndex !== 0 && cubeIndex !== 255) {
          // 计算边交点
          const edgeVertices = calculateEdgeIntersections(cubeValues, x, y, z, isoLevel)
          
          // 根据查找表生成三角形
          const triConfig = triTable_[cubeIndex]
          for (let i = 0; triConfig[i] !== -1; i += 3) {
            const v1 = edgeVertices[triConfig[i]]
            const v2 = edgeVertices[triConfig[i + 1]]
            const v3 = edgeVertices[triConfig[i + 2]]
            
            if (v1 && v2 && v3) {
              const i1 = addVertex(vertices, vertexMap, v1)
              const i2 = addVertex(vertices, vertexMap, v2)
              const i3 = addVertex(vertices, vertexMap, v3)
              
              indices.push(i1, i2, i3)
            }
          }
        }
        
        processedCubes++
        
        // 更频繁的进度更新：每处理0.01%的立方体更新一次，或者每100个立方体更新一次
        const currentProgress = processedCubes / totalCubes
        if (currentProgress - lastProgress >= 0.0001 || processedCubes % 100 === 0) {
          lastProgress = currentProgress
          self.postMessage({
            type: 'progress',
            progress: currentProgress
          })
        }
      }
    }
  }
  
  console.log(`Worker: Marching Cubes完成: ${vertices.length / 3}个顶点, ${indices.length / 3}个三角形`)
  
  return {
    vertices: vertices,
    indices: indices,
    vertexCount: vertices.length / 3,
    triangleCount: indices.length / 3
  }
}

// 辅助函数
function getCubeValues(voxelArray, x, y, z, width, height) {
  const getVoxel = (x, y, z) => {
    // 更宽松的边界检查，允许访问边界外的体素（返回0）
    if (x < 0 || x >= width || y < 0 || y >= height || z < 0 || z >= voxelArray.length / (width * height)) {
      return 0
    }
    
    // 使用 [z][y][x] 存储顺序
    const depth = voxelArray.length / (width * height)
    const index = z * width * height + y * width + x
    
    // 调试：检查索引是否有效
    if (index < 0 || index >= voxelArray.length) {
      console.warn(`无效索引: ${index}, 坐标: (${x},${y},${z}), 数组长度: ${voxelArray.length}`)
      return 0
    }
    
    return voxelArray[index] || 0
  }

  return [
    getVoxel(x, y, z), // 0
    getVoxel(x + 1, y, z), // 1
    getVoxel(x + 1, y + 1, z), // 2
    getVoxel(x, y + 1, z), // 3
    getVoxel(x, y, z + 1), // 4
    getVoxel(x + 1, y, z + 1), // 5
    getVoxel(x + 1, y + 1, z + 1), // 6
    getVoxel(x, y + 1, z + 1) // 7
  ]
}

function getCubeIndex(cubeValues, isoLevel) {
  let cubeIndex = 0
  for (let i = 0; i < 8; i++) {
    if (cubeValues[i] > isoLevel) {
      cubeIndex |= (1 << i)
    }
  }
  return cubeIndex
}

function calculateEdgeIntersections(cubeValues, x, y, z, isoLevel) {
  const edgeVertices = new Array(12).fill(null)
  const cubeIndex = getCubeIndex(cubeValues, isoLevel)
  const edgeFlags = edgeTable[cubeIndex]

  for (let edge = 0; edge < 12; edge++) {
    if (edgeFlags & (1 << edge)) {
      const [v1, v2] = getEdgeVertices(edge)
      const val1 = cubeValues[v1]
      const val2 = cubeValues[v2]

      // 线性插值计算交点位置
      const t = (isoLevel - val1) / (val2 - val1)
      const vertex = interpolateVertex(x, y, z, edge, t)
      edgeVertices[edge] = vertex
    }
  }

  return edgeVertices
}

function getEdgeVertices(edge) {
  const edgeVertices = [
    [0, 1], [1, 2], [2, 3], [3, 0], // 底面边
    [4, 5], [5, 6], [6, 7], [7, 4], // 顶面边
    [0, 4], [1, 5], [2, 6], [3, 7] // 垂直边
  ]
  return edgeVertices[edge]
}

function interpolateVertex(x, y, z, edge, t) {
  const edgeOffsets = [
    [0.5, 0, 0], [1, 0.5, 0], [0.5, 1, 0], [0, 0.5, 0], // 底面边
    [0.5, 0, 1], [1, 0.5, 1], [0.5, 1, 1], [0, 0.5, 1], // 顶面边
    [0, 0, 0.5], [1, 0, 0.5], [1, 1, 0.5], [0, 1, 0.5] // 垂直边
  ]
  const offset = edgeOffsets[edge]
  
  return [
    x + offset[0],
    y + offset[1],
    z + offset[2]
  ]
}

// 优化的顶点去重函数 - 使用更快的数值哈希和缓存优化
function addVertex(vertices, vertexMap, vertex) {
  // 使用优化的数值哈希算法，减少哈希冲突
  const hash = ((vertex[0] * 73856093) ^ (vertex[1] * 19349663) ^ (vertex[2] * 83492791)) >>> 0
  
  // 快速缓存检查
  if (vertexMap.has(hash)) {
    return vertexMap.get(hash)
  }

  const index = vertices.length / 3
  vertices.push(vertex[0], vertex[1], vertex[2])
  vertexMap.set(hash, index)
  
  // 定期清理vertexMap以防止内存泄漏（每10000个顶点清理一次）
  if (vertexMap.size > 10000 && vertexMap.size % 10000 === 0) {
    // 保留最近使用的顶点，清理不常用的
    const keys = Array.from(vertexMap.keys())
    if (keys.length > 8000) {
      for (let i = 0; i < keys.length - 8000; i++) {
        vertexMap.delete(keys[i])
      }
    }
  }
  
  return index
}

